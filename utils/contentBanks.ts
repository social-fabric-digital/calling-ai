/**
 * Get today's daily question based on the day of the year.
 * Cycles through all questions over the course of a year.
 */
export const getTodaysQuestion = (): string => {
  const now = new Date();
  const start = new Date(now.getFullYear(), 0, 0);
  const diff = now.getTime() - start.getTime();
  const dayOfYear = Math.floor(diff / (1000 * 60 * 60 * 24));
  const questions = i18n.language?.startsWith('ru') ? DAILY_QUESTIONS_RU : DAILY_QUESTIONS;
  return questions[dayOfYear % questions.length];
};

/**
 * Get a random reflection prompt.
 */
export const getRandomReflectionPrompt = (): string => {
  const prompts = i18n.language?.startsWith('ru') ? REFLECTION_PROMPTS_RU : REFLECTION_PROMPTS;
  return prompts[Math.floor(Math.random() * prompts.length)];
};

/**
 * Get a random encouragement message.
 */
export const getRandomEncouragement = (): string => {
  const prompts = i18n.language?.startsWith('ru') ? ENCOURAGEMENTS_RU : ENCOURAGEMENTS;
  return prompts[Math.floor(Math.random() * prompts.length)];
};
